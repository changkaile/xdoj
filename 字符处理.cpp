/*标题	
字符处理

类别
流程控制
	
时间限制	
1S

内存限制	
1000Kb

问题描述	
从键盘输入一个字符，若为小写字母，则输出其对应的大写字母；若为大写字母，则输出对应的小写字母；其他字符原样输出。

输入说明	
输入一个字符

输出说明	
输出一个字符

输入样例	
样例1输入
a
样例2输入
B
样例3输入
2

输出样例	
样例1输出
A
样例2输出
b
样例3输出
2	*/ 
#include"stdio.h"
int main()
{
	char c;
	scanf("%c",&c);
	if(c>='a'&&c<='z')
	{
		c=c-32;
		printf("%c",c);
	}
	else if(c>='A'&&c<='Z')
	{
		c=c+32;
		printf("%c",c);
	}
	else
	{
		printf("%c",c);
	}
	return 0;
}
